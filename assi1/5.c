//print ASCII values of escape sequence
#include <stdio.h>

int main()
{
    printf("ASCII value of \\n is : %d\n", '\n');
    printf("ASCII value of \\r is : %d\n", '\r');
    printf("ASCII value of \\t is : %d\n", '\t');
    printf("ASCII value of \\a is : %d\n", '\a');
    printf("ASCII value of \\b is : %d\n", '\b');
    return 0;
}